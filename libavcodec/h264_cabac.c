    significant_coeff_ctx_base = h->cabac_state
        + significant_coeff_flag_offset[MB_FIELD(h)][cat];
    last_coeff_ctx_base = h->cabac_state
        + last_coeff_flag_offset[MB_FIELD(h)][cat];
